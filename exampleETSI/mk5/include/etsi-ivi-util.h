/// @brief Set provided country code in (2 octet) buffer
/// @param pISO3166Code Pointer to upper case country code string (e.g. "AU")
/// @param pBuf Pointer to store country code array (at least 2 octets)
/// @return Status Code (0 = success)
int ETSIIVI_SetISO31661Code(const char *pISO3166Code, uint8_t *pBuf);

/// @brief Get (upper case) country code in provided (2 octet) buffer
/// @param pBuf Pointer to country code buffer
/// @param pISO3166Code Pointer to store country code string (at least 3 chars)
/// @return Status Code (0 = success)
int ETSIIVI_GetISO31661Code(const uint8_t *pBuf, char *pISO3166Code);

/// @brief Set provided language code in (2 octet) buffer
/// @param pISO639Code Pointer to lower case language code string (e.g. "en")
/// @param pBuf Pointer to store language code array (at least 2 octets)
/// @return Status Code (0 = success)
int ETSIIVI_SetISO6391Code(const char *pISO639Code, uint8_t *pBuf);

/// @brief Get (lower case) language code in provided (2 octet) buffer
/// @param pBuf Pointer to language code buffer
/// @param pISO639Code Pointer to language code string (at least 3 chars)
/// @return Status Code (0 = success)
int ETSIIVI_GetISO6391Code(const uint8_t *pBuf, char *pISO639Code);

/// @brief Integer value for Service Provider
/// @param pProvider Pointer to structure representation of ITSProvider
/// @return Numerical value of Service Provider (or 0 on error)
uint32_t ETSIIVI_GetServiceProvider(const ITSProvider *pProvider);

/// @brief Set text and language code in provided ASN.1 ITS Text structure
/// @param pISO639Code Pointer to lower case language code string
/// @param pStr Pointer to IVS text (ASCII string)
/// @param CompId Optional Component Id (1-8, 0 for not used)
/// @param pITSText Pointer to ITS Text structure (buffers for strings will be allocated)
/// @return Status Code (0 = success)
int ETSIIVI_SetIVSText(const char *pISO639Code, const char *pStr, uint8_t CompId, ITSText *pITSText);

/// @brief Get Pictogram Code
/// @param pITSISO14823Code Pointer to pictogram code sequence
/// @return Full pictogram code
tIVIPictogramCode ETSIIVI_GetPictogramCode(const ITSISO14823Code *pITSISO14823Code);

/// @brief Get ASCII text content from Text structure
///        Note: Multiple Text items are concatenated with a newline
/// @param TextCount Number of Text items to concatenate
/// @param pText Pointer to first ITSText element
/// @param BufferSize Size of the storage for formed string
/// @param pBuffer Pointer to storage for formed string
/// @return Size of formed ASCII string
size_t ETSIIVI_GetMultipleText(size_t TextCount, const ITSText *pText, size_t BufferSize, char *pBuffer);

